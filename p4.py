num1 = int(input('enter a first number:'))
num2 = int(input('enter a second number:'))
#before swapping
print('Numbers before swapping')
print('first number:',num1)
print('second number:',num2)
#logic for swapping
num1, num2 = num2, num1
#after swapping
print('Numbers after swapping')
print('first number:',num1)
print('second number:',num2)